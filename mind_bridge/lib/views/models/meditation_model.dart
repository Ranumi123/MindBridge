class Meditation {
  final String title;
  final String duration;
  final double rating;
  final String author;
  final String imageUrl;
  final String description;
  final String url;

  const Meditation({
    required this.title,
    required this.duration,
    required this.rating,
    required this.author,
    required this.imageUrl,
    required this.description,
    required this.url,
  });
}
